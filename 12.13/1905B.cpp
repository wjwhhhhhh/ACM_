void solve()
{
    int n;
    cin >> n;
    vector<int> deg(n);
    for (int i = 1; i < n; i++)
    {
        int a, b;
        cin >> a >> b;
        a--, b--;
        deg[a]++, deg[b]++;
    }
    int ans = 0;
    for (auto c : deg)
    {
        ans += (c == 1);
    }
    cout << (ans + 1) / 2 << endl;
}
