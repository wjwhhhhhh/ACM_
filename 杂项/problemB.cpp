void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];
    int ma = *max_element(a.begin(), a.end());
    for (int i = 1; i < n; i++)
    {
        if (a[i] == ma || a[i - 1] == ma)
            continue;
        if (abs(a[i] - a[i - 1]) > 2)
        {
            cout << "No" << endl;
            return;
        }
    }
    cout << "Yes" << endl;
}
